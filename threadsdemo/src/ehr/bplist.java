package ehr;

public class bplist {
    String name;
    int systolicpre;
    int diastolicpre;
    String summary;
    String detail;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSystolicpre() {
        return systolicpre;
    }

    public void setSystolicpre(int systolicpre) {
        this.systolicpre = systolicpre;
    }

    public int getDiastolicpre() {
        return diastolicpre;
    }

    public void setDiastolicpre(int diastolicpre) {
        this.diastolicpre = diastolicpre;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }
}
